void main() {
  // Increment And Decrement Operators
  // Prefix increment and decrement operators
  int a = 10;
  print(a); // 10

  print(++a); // 11

  print(--a); // 10

  print("====================");

  // Postfix increment and decrement operators
  int b = 20;
  print(b); // 20

  print(b++); // 20 (prints before incrementing)

  print(b); // 21
}
